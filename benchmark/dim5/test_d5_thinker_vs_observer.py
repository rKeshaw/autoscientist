"""
Dimension 5 - Test 8: Thinker vs Observer Retrieval Quality
=============================================================
Tests whether questions generated by the Thinker (via reductive
decomposition) lead to better research retrieval outcomes than questions
surfaced by the Observer (from dream cycles and graph monitoring).

This is the ROADMAP-specified comparison test:
  "thinker-question vs observer-question retrieval quality"

The test:
1. Runs a Thinker session to generate sub-questions
2. Uses existing Observer agenda items as comparison
3. Feeds both sets of questions into the Researcher
4. Compares retrieval relevance of results from each source

Benchmark level:
  - pipeline-level
"""

import json
import os
import sys
import time
import argparse
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from _shared import (
    get_isolated_graph, make_researcher, judge_json,
    DEFAULT_MISSION,
)


RETRIEVAL_JUDGE = """
You are evaluating whether a search result is useful for answering a
research sub-question.

ORIGINAL MISSION: "{mission}"
SUB-QUESTION: "{question}"
SOURCE: {source}

SEARCH RESULT TITLE: "{title}"
SEARCH RESULT TEXT: "{text}"

Evaluate:
1. **Direct relevance**: Does the result help answer the sub-question?
2. **Mission advancement**: Does the result contribute to answering the mission?
3. **Specificity**: Is the information specific and actionable (vs generic)?

Score:
- relevance: 1 to 7 (how relevant to the sub-question)
- mission_value: 1 to 7 (how much it advances the mission)

Respond EXACTLY in JSON:
{{
  "relevant": true or false,
  "relevance": 1 to 7,
  "mission_value": 1 to 7,
  "reasoning": "one sentence"
}}
"""


# Observer-generated questions (simulate typical agenda items from dream
# cycles). These represent the kind of questions the Observer surfaces by
# monitoring dream cycle outputs and graph structure.
OBSERVER_QUESTIONS = [
    "What is the relationship between synaptic plasticity and learning?",
    "How does dopamine modulate reward-based decision making?",
    "What role does entropy play in biological information processing?",
]

# We'll generate Thinker questions dynamically by running a reductive
# decomposition on the mission.


def _search_question(researcher, question: str, n_queries: int = 2):
    """Search for a question and return filtered results."""
    queries = researcher._generate_queries(question, n=n_queries)
    all_results = []
    query_breakdown = []
    for query in queries:
        raw_results = researcher._web_search(query)
        filtered_results = researcher._filter_relevant(question, raw_results)
        query_breakdown.append({
            "query": query,
            "raw_results_count": len(raw_results),
            "filtered_results_count": len(filtered_results),
        })
        for title, text, url in filtered_results:
            all_results.append({
                "query": query,
                "title": title,
                "text": text,
                "url": url,
            })
        time.sleep(1.5)  # rate limiting
    return queries, all_results, query_breakdown


def _evaluate_results(results, question, mission, source, judge_model):
    """Evaluate search results for a question using the LLM judge."""
    evaluations = []
    relevant_count = 0
    total_relevance = 0.0
    total_mission = 0.0

    for r in results:
        judgment = judge_json(
            RETRIEVAL_JUDGE.format(
                mission=mission,
                question=question,
                source=source,
                title=r["title"][:200],
                text=r["text"][:500],
            ),
            model=judge_model,
            default={
                "relevant": False, "relevance": 1,
                "mission_value": 1, "reasoning": "Parse failed",
            },
        )

        is_relevant = bool(judgment.get("relevant", False))
        relevance = float(judgment.get("relevance", 1))
        mission_val = float(judgment.get("mission_value", 1))

        if is_relevant:
            relevant_count += 1
        total_relevance += relevance
        total_mission += mission_val

        evaluations.append({
            "query": r["query"],
            "title": r["title"][:200],
            "relevant": is_relevant,
            "relevance": relevance,
            "mission_value": mission_val,
            "judgment": judgment,
        })

    n = max(len(results), 1)
    return {
        "evaluations": evaluations,
        "results_count": len(results),
        "relevant_count": relevant_count,
        "precision": round(relevant_count / n, 3),
        "mean_relevance": round(total_relevance / n, 3),
        "mean_mission_value": round(total_mission / n, 3),
    }


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--judge-model", default="llama3.1:70b")
    parser.add_argument("--skip-network", action="store_true", default=False)
    parser.add_argument(
        "--out",
        default="benchmark/dim5/results/d5_thinker_vs_observer.json",
    )
    args = parser.parse_args()

    if args.skip_network:
        print("TEST 8: Thinker vs Observer — SKIPPED (--skip-network)")
        report = {
            "test": "D5 - Thinker vs Observer Retrieval Quality",
            "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
            "summary": {
                "PASS": False,
                "skipped": True,
                "skip_reason": "Network-dependent benchmark skipped by user request.",
            },
        }
        os.makedirs(os.path.dirname(args.out), exist_ok=True)
        with open(args.out, "w") as f:
            json.dump(report, f, indent=2)
        return

    os.makedirs(os.path.dirname(args.out), exist_ok=True)

    print("=" * 60)
    print("TEST 8: Thinker vs Observer Retrieval Quality")
    print("=" * 60)

    # ── Phase 1: Generate Thinker sub-questions ──────────────────────────────

    print("\n  Phase 1: Generating Thinker sub-questions via reductive decomposition")

    from thinker.thinker import Thinker
    from observer.observer import Observer
    from graph.brain import Brain
    from embedding_index import EmbeddingIndex

    brain, emb_index = get_isolated_graph(DEFAULT_MISSION)
    observer = Observer(brain)
    thinker = Thinker(brain, observer=observer, embedding_index=emb_index)

    # Run a reductive thinking session to generate sub-questions
    thinker_log = thinker.think(
        question=DEFAULT_MISSION,
        pattern="reductive",
    )

    thinker_questions = []
    for sq in thinker_log.sub_questions:
        q = sq.get("question", "") if isinstance(sq, dict) else str(sq)
        if q:
            thinker_questions.append(q)

    if not thinker_questions:
        print("  WARNING: Thinker produced no sub-questions.")
        report = {
            "test": "D5 - Thinker vs Observer Retrieval Quality",
            "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
            "config": {
                "judge_model": args.judge_model,
                "mission": DEFAULT_MISSION,
            },
            "summary": {
                "thinker_questions_count": 0,
                "observer_questions_count": len(OBSERVER_QUESTIONS),
                "benchmark_exercised": False,
                "failure_reason": "Thinker produced no sub-questions for comparison.",
                "PASS": False,
            },
            "thinker": {"questions": [], "evaluations": {}},
            "observer": {"questions": OBSERVER_QUESTIONS, "evaluations": {}},
        }
        with open(args.out, "w", encoding="utf-8") as f:
            json.dump(report, f, indent=2)
        print(f"Full report saved to: {args.out}")
        return

    print(f"  Thinker generated {len(thinker_questions)} sub-questions")
    for i, q in enumerate(thinker_questions[:5]):
        print(f"    T{i+1}: {q[:70]}...")

    print(f"\n  Observer has {len(OBSERVER_QUESTIONS)} agenda questions")
    for i, q in enumerate(OBSERVER_QUESTIONS):
        print(f"    O{i+1}: {q[:70]}...")

    # ── Phase 2: Research both sets of questions ─────────────────────────────

    print("\n  Phase 2: Researching Thinker questions")

    # Use a fresh researcher for each set
    researcher_t, _, _, _ = make_researcher(depth="standard")

    thinker_results = {}
    for q in thinker_questions[:3]:  # cap at 3 to keep test time reasonable
        print(f"\n    Researching: {q[:60]}...")
        queries, results, query_breakdown = _search_question(researcher_t, q)
        thinker_results[q] = {
            "queries": queries,
            "query_breakdown": query_breakdown,
            "results": results,
        }
        print(f"      → {len(queries)} queries, {len(results)} results")

    print("\n  Phase 3: Researching Observer questions")

    researcher_o, _, _, _ = make_researcher(depth="standard")

    observer_results = {}
    for q in OBSERVER_QUESTIONS:
        print(f"\n    Researching: {q[:60]}...")
        queries, results, query_breakdown = _search_question(researcher_o, q)
        observer_results[q] = {
            "queries": queries,
            "query_breakdown": query_breakdown,
            "results": results,
        }
        print(f"      → {len(queries)} queries, {len(results)} results")

    # ── Phase 3: Evaluate both sets ──────────────────────────────────────────

    print("\n  Phase 4: Evaluating retrieval quality")

    thinker_evals = {}
    for q, data in thinker_results.items():
        print(f"\n    [Thinker] {q[:50]}...")
        ev = _evaluate_results(
            data["results"], q, DEFAULT_MISSION,
            "Thinker (reductive decomposition)",
            args.judge_model,
        )
        thinker_evals[q] = ev
        print(f"      Precision: {ev['precision']:.0%}, "
              f"Relevance: {ev['mean_relevance']:.1f}/7, "
              f"Mission: {ev['mean_mission_value']:.1f}/7")

    observer_evals = {}
    for q, data in observer_results.items():
        print(f"\n    [Observer] {q[:50]}...")
        ev = _evaluate_results(
            data["results"], q, DEFAULT_MISSION,
            "Observer (agenda monitoring)",
            args.judge_model,
        )
        observer_evals[q] = ev
        print(f"      Precision: {ev['precision']:.0%}, "
              f"Relevance: {ev['mean_relevance']:.1f}/7, "
              f"Mission: {ev['mean_mission_value']:.1f}/7")

    # ── Aggregate comparison ─────────────────────────────────────────────────

    def _aggregate(evals):
        if not evals:
            return {"precision": 0, "mean_relevance": 0, "mean_mission_value": 0}
        n = len(evals)
        return {
            "precision": round(sum(e["precision"] for e in evals.values()) / n, 3),
            "mean_relevance": round(sum(e["mean_relevance"] for e in evals.values()) / n, 3),
            "mean_mission_value": round(sum(e["mean_mission_value"] for e in evals.values()) / n, 3),
        }

    thinker_agg = _aggregate(thinker_evals)
    observer_agg = _aggregate(observer_evals)
    thinker_result_total = sum(ev["results_count"] for ev in thinker_evals.values())
    observer_result_total = sum(ev["results_count"] for ev in observer_evals.values())
    benchmark_exercised = thinker_result_total > 0 and observer_result_total > 0

    print("\n  ── Aggregate Comparison ──")
    print(f"    Thinker:  precision={thinker_agg['precision']:.0%}, "
          f"relevance={thinker_agg['mean_relevance']:.1f}/7, "
          f"mission={thinker_agg['mean_mission_value']:.1f}/7")
    print(f"    Observer: precision={observer_agg['precision']:.0%}, "
          f"relevance={observer_agg['mean_relevance']:.1f}/7, "
          f"mission={observer_agg['mean_mission_value']:.1f}/7")

    thinker_advantage = (
        thinker_agg["mean_relevance"] - observer_agg["mean_relevance"]
    )
    thinker_mission_advantage = (
        thinker_agg["mean_mission_value"] - observer_agg["mean_mission_value"]
    )
    precision_delta = thinker_agg["precision"] - observer_agg["precision"]
    relevance_floor = thinker_agg["mean_relevance"] >= (observer_agg["mean_relevance"] - 0.25)
    mission_floor = thinker_agg["mean_mission_value"] >= (observer_agg["mean_mission_value"] - 0.25)
    precision_floor = thinker_agg["precision"] >= (observer_agg["precision"] - 0.10)
    not_losing_both = not (
        thinker_agg["mean_relevance"] < observer_agg["mean_relevance"] and
        thinker_agg["mean_mission_value"] < observer_agg["mean_mission_value"]
    )
    passed = (
        benchmark_exercised and
        relevance_floor and
        mission_floor and
        precision_floor and
        not_losing_both
    )

    print(f"\n    Thinker advantage (relevance): {thinker_advantage:+.2f}")
    print(f"    Thinker advantage (mission):   {thinker_mission_advantage:+.2f}")
    print(f"    Thinker advantage (precision): {precision_delta:+.2f}")

    report = {
        "test": "D5 - Thinker vs Observer Retrieval Quality",
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
        "config": {
            "judge_model": args.judge_model,
            "mission": DEFAULT_MISSION,
        },
        "summary": {
            "thinker_questions_count": len(thinker_questions),
            "observer_questions_count": len(OBSERVER_QUESTIONS),
            "thinker_precision": thinker_agg["precision"],
            "thinker_mean_relevance": thinker_agg["mean_relevance"],
            "thinker_mean_mission_value": thinker_agg["mean_mission_value"],
            "observer_precision": observer_agg["precision"],
            "observer_mean_relevance": observer_agg["mean_relevance"],
            "observer_mean_mission_value": observer_agg["mean_mission_value"],
            "relevance_advantage": round(thinker_advantage, 3),
            "mission_advantage": round(thinker_mission_advantage, 3),
            "precision_advantage": round(precision_delta, 3),
            "thinker_results_total": thinker_result_total,
            "observer_results_total": observer_result_total,
            "benchmark_exercised": benchmark_exercised,
            "PASS": passed,
        },
        "thinker": {
            "questions": thinker_questions[:3],
            "evaluations": {
                q: {
                    **{k: v for k, v in thinker_results[q].items() if k != "results"},
                    **{k: v for k, v in ev.items() if k != "evaluations"},
                }
                for q, ev in thinker_evals.items()
            },
        },
        "observer": {
            "questions": OBSERVER_QUESTIONS,
            "evaluations": {
                q: {
                    **{k: v for k, v in observer_results[q].items() if k != "results"},
                    **{k: v for k, v in ev.items() if k != "evaluations"},
                }
                for q, ev in observer_evals.items()
            },
        },
    }

    with open(args.out, "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2)

    verdict = "PASS" if passed else "FAIL"
    print(f"\nOVERALL VERDICT: {verdict}")
    print(f"Full report saved to: {args.out}")


if __name__ == "__main__":
    main()
