"""
Dimension 2 — Test 2: Insight Validity
========================================
Validates "structural" or "isomorphism" depth insights generated by the dreamer.
Uses an LLM judge to determine if claimed structural parallels actually hold or
are merely shared vocabulary.

Pass criterion:
- Fraction of claimed deep insights that survive review > 60%.
- The benchmark must actually exercise both deep-insight slices it claims to
  test: at least 5 structural insights and at least 2 isomorphism insights.

Benchmark level:
  - module-level

Usage:
    python benchmark/dim2/test_d2_insight_validity.py \
        --judge-model <ollama-model-name> \
        --out benchmark/dim2/results/d2_insight_validity.json
"""

import os
import sys
import json
import time
import argparse
import random
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT))

CORPUS = [
    {"id": "dna",              "title": "DNA"},
    {"id": "thermodynamics",   "title": "Thermodynamics"},
    {"id": "natural_selection","title": "Natural selection"},
    {"id": "neural_network",   "title": "Artificial neural network"},
    {"id": "game_theory",      "title": "Game theory"},
]

JUDGE_PROMPT = """
You are evaluating the quality of an "insight" generated by an AI research agent.
The agent claimed to find a structural or isomorphic connection between these two concepts.

Concept A: "{node_a}"
Concept B: "{node_b}"
Agent's Insight Connection: "{narration}"
Claimed Depth: {depth}

Analyze this connection. Does the claimed deep parallel actually hold?
A genuine "structural" or "isomatic" connection must map equivalent underlying mechanics or formal relationships (e.g., A relates to B as X relates to Y).
A false connection simply relies on shared generic vocabulary (e.g., "both have states" or "both use the word energy").

Respond EXACTLY in this JSON format:
{{
  "genuine": true or false,
  "reasoning": "<1-2 sentence explanation>"
}}
"""

def fetch_wikipedia(title: str) -> str:
    import requests
    api = "https://en.wikipedia.org/w/api.php"
    params = {
        "action": "query", "titles": title,
        "prop": "extracts", "format": "json",
    }
    resp = requests.get(api, params=params, timeout=20,
                        headers={"User-Agent": "AutoScientist-Benchmark/2.0"})
    pages = resp.json().get("query", {}).get("pages", {})
    for page in pages.values():
        return page.get("extract", "")[:8000]
    return ""

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--judge-model", default="llama3.1:70b")
    parser.add_argument("--out", default="benchmark/dim2/results/d2_insight_validity.json")
    parser.add_argument("--min-structural", type=int, default=5)
    parser.add_argument("--min-isomorphism", type=int, default=2)
    args = parser.parse_args()

    os.makedirs(os.path.dirname(args.out), exist_ok=True)

    from graph.brain import Brain
    from embedding_index import EmbeddingIndex
    from ingestion.ingestor import Ingestor
    from observer.observer import Observer
    from dreamer.dreamer import Dreamer

    brain = Brain()
    emb_index = EmbeddingIndex(dimension=384)
    
    # Load shared brain and index if available to save time
    brain_path = "benchmark/dim2/shared/brain.json"
    index_path = "benchmark/dim2/shared/embedding_index"
    
    if os.path.exists(brain_path) and os.path.exists(index_path + ".json"):
        print("  Loading shared brain and index...")
        brain.load(brain_path)
        emb_index = EmbeddingIndex.load(index_path)
    else:
        print("  Shared brain not found. Ingesting from scratch...")
        observer = Observer(brain)
        ingestor = Ingestor(brain, research_agenda=observer, embedding_index=emb_index)
        from graph.brain import EdgeSource
        for article in CORPUS:
            print(f"  Ingesting: {article['title']}...")
            text = fetch_wikipedia(article["title"])
            if text:
                ingestor.ingest(text, source=EdgeSource.READING)
                time.sleep(1)

    observer = Observer(brain)
    dreamer = Dreamer(brain, research_agenda=observer)

    print("\n" + "=" * 60)
    print("PHASE 2: Forcing Insights via Dreamer")
    print("=" * 60)
    insights = []
    seen = set()
    # Run dream cycles to accumulate insights
    for i in range(5):
        print(f"  Running Dream Cycle {i+1}/5...")
        # Higher temperature might trigger more diverse connections/insights
        log = dreamer.dream(steps=20, temperature=0.85, run_nrem=False)
        for ins in log.insights:
            if ins.get('depth') in ['structural', 'isomorphism']:
                key = (
                    ins.get('from_node_id'),
                    ins.get('to_node_id'),
                    ins.get('narration'),
                    ins.get('depth'),
                )
                if key in seen:
                    continue
                seen.add(key)
                insights.append(ins)

    raw_depth_counts = Counter(
        ins.get('raw_depth', ins.get('depth', 'unknown')) for ins in insights
    )
    depth_counts = Counter(ins.get('depth', 'unknown') for ins in insights)
    structural_count = depth_counts.get('structural', 0)
    isomorphism_count = depth_counts.get('isomorphism', 0)
    exercised_structural = structural_count >= args.min_structural
    exercised_isomorphism = isomorphism_count >= args.min_isomorphism
    exercised_depth_mix = exercised_structural and exercised_isomorphism

    if not insights:
        print("No structural or isomorphic insights produced.")
        report = {
            "test": "D2 — Insight Validity",
            "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
            "config": {
                "judge_model": args.judge_model,
                "min_structural": args.min_structural,
                "min_isomorphism": args.min_isomorphism,
            },
            "summary": {
                "num_deep_insights": 0,
                "genuine_count": 0,
                "genuine_fraction": 0.0,
                "benchmark_exercised": False,
                "benchmark_exercised_structural": False,
                "benchmark_exercised_isomorphism": False,
                "benchmark_exercised_depth_mix": False,
                "failure_reason": "Dreamer produced no structural or isomorphic insights.",
                "PASS": False,
            },
            "evaluations": [],
        }
        with open(args.out, "w") as f:
            json.dump(report, f, indent=2)
        print(f"Full report saved to: {args.out}")
        return

    print("\n" + "=" * 60)
    print(f"PHASE 3: Evaluating {len(insights)} Deep Insights")
    print("=" * 60)

    from llm_utils import llm_call, require_json
    results = []
    passed_count = 0

    for i, ins in enumerate(insights):
        prompt = JUDGE_PROMPT.format(
            node_a=ins['from'],
            node_b=ins['to'],
            narration=ins['narration'],
            depth=ins['depth']
        )
        raw = llm_call(prompt, temperature=0.1, model=args.judge_model, role="precise")
        try:
             res = require_json(raw, default={
                 "genuine": False,
                 "reasoning": "Judge parse failed",
             })
        except:
             res = {
                 "genuine": False,
                 "reasoning": "Judge parse failed",
             }
        
        results.append({
            "insight": ins,
            "judgment": res
        })
        if res.get('genuine'):
            passed_count += 1
        time.sleep(0.3)

    fraction = passed_count / max(len(insights), 1)
    passed = fraction >= 0.60 and exercised_depth_mix

    report = {
        "test": "D2 — Insight Validity",
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
        "config": {
            "judge_model": args.judge_model,
            "min_structural": args.min_structural,
            "min_isomorphism": args.min_isomorphism,
        },
        "summary": {
            "num_deep_insights": len(insights),
            "genuine_count": passed_count,
            "genuine_fraction": round(fraction, 3),
            "raw_depth_counts": dict(raw_depth_counts),
            "depth_counts": dict(depth_counts),
            "structural_count": structural_count,
            "isomorphism_count": isomorphism_count,
            "benchmark_exercised": len(insights) > 0,
            "benchmark_exercised_structural": exercised_structural,
            "benchmark_exercised_isomorphism": exercised_isomorphism,
            "benchmark_exercised_depth_mix": exercised_depth_mix,
            "PASS": passed
        },
        "evaluations": results
    }

    with open(args.out, "w") as f:
        json.dump(report, f, indent=2)

    print("\n" + "=" * 60)
    print("RESULTS — D2: Insight Validity")
    print("=" * 60)
    print(f"Total structured/isomorphic insights evaluated: {len(insights)}")
    print(f"Depth mix                                    : structural={structural_count}, isomorphism={isomorphism_count}")
    print(f"Insights judged as genuine deep parallels     : {passed_count}")
    print(f"Validity rate                                 : {fraction:.2%} (pass >= 60%)")
    print(f"Depth mix exercised                           : {exercised_depth_mix} (need >= {args.min_structural} structural and >= {args.min_isomorphism} isomorphism)")
    verdict = "PASS ✓" if passed else "FAIL ✗"
    print(f"\nOVERALL VERDICT: {verdict}")
    print(f"Full report saved to: {args.out}")

if __name__ == "__main__":
    main()
